// Площадь круга по двум катетам
//
// Имея 2 катета a и b прямоугольного треугольника найти площадь вписанного в него круга.
// Результат округлить до двух знаков после запятой. Pi принять равным 3.14. 1 <= a, b <= 100
//
// Пример
//
// > node task.js 6 8
// > 12.56
var Pi = 3.14;
var a = +process.argv[2];
var b = +process.argv[3];
var c = Math.sqrt(a * a + b * b);
var r = (a + b - c) / 2;
var S = Pi * r * r;
console.log(S.toFixed(2));